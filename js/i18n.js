let currentLang = localStorage.getItem("js_lang") || "zh";
function t(key){ return (I18N[currentLang] && I18N[currentLang][key]) || I18N.zh[key] || key; }
function setLang(lang){
  currentLang = lang;
  localStorage.setItem("js_lang", lang);
  document.documentElement.lang = lang;
  applyI18n();
  document.querySelectorAll(".lang-switch button").forEach(b=>b.classList.toggle("active", b.dataset.lang===lang));
  window.dispatchEvent(new CustomEvent("langchange",{detail:lang}));
}
function applyI18n(){
  document.querySelectorAll("[data-i18n]").forEach(el=>{ el.textContent = t(el.dataset.i18n); });
  document.querySelectorAll("[data-i18n-ph]").forEach(el=>{ el.placeholder = t(el.dataset.i18nPh); });
}
window.t = t; window.setLang = setLang; window.applyI18n = applyI18n;
Object.defineProperty(window,"currentLang",{get:()=>currentLang});
